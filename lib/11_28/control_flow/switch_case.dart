// 이 Dart 코드는 제어문 예시를 포함하고 있습니다.

void main() {
  //switch-case 문: 다중 분기 처리
  String fruit = 'apple';
  switch (fruit) {
    case 'apple':
      print('Apple pie is delicious.');
      break;
    case 'banana':
      print('banana is good for your health.');
      break;
    case 'orange':
      print('orange juice is refreshing.');
      break;
    default:
      print('I like other fruits.');
  }
}
